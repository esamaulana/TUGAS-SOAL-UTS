package model;

/**
 *
 * @author faldi
 */
public class Pengunjung {
    private String noKTP;
    private String nama;
    private String jenKel;
    private String handuk;
    private String cabang;
    private String paketFitness;

    public String getNoKTP() {
        return noKTP;
    }

    public void setNoKTP(String noKTP) {
        this.noKTP = noKTP;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenKel() {
        return jenKel;
    }

    public void setJenKel(String jenKel) {
        this.jenKel = jenKel;
    }

    public String getHanduk() {
        return handuk;
    }

    public void setHanduk(String handuk) {
        this.handuk = handuk;
    }

    public String getCabang() {
        return cabang;
    }

    public void setCabang(String cabang) {
        this.cabang = cabang;
    }

    public String getPaketFitness() {
        return paketFitness;
    }

    public void setPaketFitness(String paketFitness) {
        this.paketFitness = paketFitness;
    }
    
    }
